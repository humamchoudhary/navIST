import folium

class Node:
    def __init__ (self, lat, lon, name, info , custom_icon, next = None):
        self.lat = lat
        self.lon = lon
        self.name = name
        self.info = info
        self.custom_icon = custom_icon
        self.next = next

class LinkedList:
    def __init__(self):
        self.head = None


    def add_node(self, lat, lon, name, info, custom_icon):
        new_node = Node(lat, lon, name, info, custom_icon)
        new_node.next = self.head
        self.head = new_node


